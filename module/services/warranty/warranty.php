                <li>Serial Number/IMEI/Service Tag must be intact, clearly visible, and match the warranty card if provided.</li>
                <li>For products purchased before October 15, 2022 or without Serial/IMEI tracking, the Technologia warranty sticker must be intact and legible.</li>
            </ul>
            <p>Cases Not Covered by Warranty:</p>
            <ul>
                <li>Product does not meet manufacturer’s warranty conditions.</li>
                <li>Violation of the manufacturer’s warranty terms.</li>
                <li>Damage not caused by manufacturer hardware defects.</li>
                <li>Damage due to wear and tear, natural disasters, external/physical impact, environmental conditions, dust, or moisture/liquid exposure.</li>
                <li>Missing or unreadable manufacturer serial number.</li>
                <li>Unable to verify legal purchase origin.</li>
                <li>Product used outside its intended purpose.</li>
                <li>Damage caused by non-genuine components or accessories.</li>
                <li>Warranty rejection is at the discretion of the manufacturer based on their policy.</li>
            </ul>

